PRAGMA FOREIGN_KEYS = ON;

CREATE TABLE IF NOT EXISTS F_RESULT (
  GAME_ID INTEGER NOT NULL PRIMARY KEY,
  TOURNAMENT_CODE INTEGER NOT NULL REFERENCES D_TOURNAMENT(TOURNAMENT_CODE),
  WINNER_ID INTEGER NOT NULL REFERENCES D_TEAM(TEAM_ID),
  WINNER_SCORE INTEGER NOT NULL,
  LOSE_ID INTEGER NOT NULL REFERENCES D_TEAM(TEAM_ID),
  LOSE_SCORE INTEGER NOT NULL,
  CREATED_AT TEXT NOT NULL DEFAULT (DATETIME('now', 'localtime')),
  EDITED_AT TEXT NOT NULL DEFAULT (DATETIME('now', 'localtime'))
);

CREATE TRIGGER TRIGGER_F_RESULT_UPDATED_AT AFTER
  UPDATE ON F_RESULT
BEGIN
  UPDATE F_RESULT
  SET
    UPDATED_AT = DATETIME(
      'now',
      'localtime'
    )
  WHERE
    ROWID == NEW.ROWID;
END;