PRAGMA FOREIGN_KEYS = ON;

CREATE TABLE IF NOT EXISTS F_GAME_RESULT (
  ID INTEGER PRIMARY KEY,
  TOURNAMENT_CODE INTEGER REFERENCE D_TOURNAMENT(CODE),
  VICTORY_ID INTEGER REFERENCE D_TEAM(ID),
  VICTORY_SCORE INTEGER,
  LOSE_ID INTEGER REFERENCE D_TEAM(ID),
  LOSE_SCORE INTEGER,
  CREATED_AT TEXT NOT NULL DEFAULT (DATETIME('now', 'localtime')),
  EDITED_AT TEXT NOT NULL DEFAULT (DATETIME('now', 'localtime'))
);

CREATE TRIGGER TRIGGER_F_GAME_RESULT_UPDATED_AT AFTER
  UPDATE ON F_GAME_RESULT
BEGIN
  UPDATE F_GAME_RESULT
  SET
    UPDATED_AT = DATETIME(
      'now',
      'localtime'
    )
  WHERE
    ROWID == NEW.ROWID;
END;